export function colorPieza(numero) {
    const colores = [
        "bg-white",
        "bg-secondary",
        "bg-primary",
        "bg-black",
        "bg-success",
        "bg-danger",
        "bg-warning",
        "bg-info",
        "bg-light",
        "bg-dark"
      ]

      return(colores[numero])
}